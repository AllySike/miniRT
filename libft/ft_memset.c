#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	char	*dest;

	if (b == NULL)
		return (NULL);
	dest = b;
	while (len-- > 0)
	{
		*dest++ = c;
	}
	return (b);
}
